<p> 各行各業幾乎都有自己熟悉的暗語，別人聽起來無厘頭又晦澀難解，</p>
<p>不過若是瞭解了，下次聽到總會讓人會心一笑～～～<br /> 有網友在Dcard上貼文，表示她參加畢業旅行團時，<br /> 曾經不睡覺仔細聽「頭車」司機的對話，<br /> 而第一輛車代表最重要的角色，必須告知後面跟車的司機前面的路況，也因此意外聽到了各種神奇「術語」。</p>
<p> <img src="/uploads/1479273688-sGb0o.jpg" alt="" width="753" height="565" /></p>
<p> 女網友說，例如司機看到對向道兩輛小客車開過去，就會報<strong>「烏龜兩隻」</strong>；</p>
<p> <img src="/uploads/1479273688-Vjo3e.jpg" alt="" /></p>
<p> 若是看到車子拋錨，就會說<span style="font-size: x-large;">「烏龜中風」</span></p>
